package com.kivvi.ripples.config.parser;


public class configInfo {
	private String frequencyswitch;
	private String rangeswitch;
	private boolean scrollbackgroundswitch;
	private int backgroundoption;
	private String custombackgroundpath;
	private boolean reflectlight;
	
	public String getFrequencyswitch() {
		return frequencyswitch;
	}
	public void setFrequencyswitch(String frequencyswitch) {
		this.frequencyswitch = frequencyswitch;
	}
	public String getRangeswitch() {
		return rangeswitch;
	}
	public void setRangeswitch(String rangeswitch) {
		this.rangeswitch = rangeswitch;
	}
	public boolean isScrollbackgroundswitch() {
		return scrollbackgroundswitch;
	}
	public void setScrollbackgroundswitch(boolean scrollbackgroundswitch) {
		this.scrollbackgroundswitch = scrollbackgroundswitch;
	}
	public int getBackgroundoption() {
		return backgroundoption;
	}
	public void setBackgroundoption(int backgroundoption) {
		this.backgroundoption = backgroundoption;
	}
	public String getCustombackgroundpath() {
		return custombackgroundpath;
	}
	public void setCustombackgroundpath(String custombackgroundpath) {
		this.custombackgroundpath = custombackgroundpath;
	}
	public boolean getReflectlight() {
		return reflectlight;
	}
	public void setReflectlight(boolean reflectlight) {
		this.reflectlight = reflectlight;
	}
	
	
	
}

